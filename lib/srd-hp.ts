/** Look up a creature name and return its HP dice formula, or null if unknown. */
export function lookupHpRoll(name: string): string | null {
  const key = name.trim().toLowerCase();
  if (SRD_HP[key]) return SRD_HP[key];

  // Try partial match — e.g. "Goblin Archer" → "goblin"
  for (const [creature, dice] of Object.entries(SRD_HP)) {
    if (key.startsWith(creature) || key.endsWith(creature)) return dice;
  }
  return null;
}
