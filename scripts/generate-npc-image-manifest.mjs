#!/usr/bin/env node
/**
 * Scans public/images/NPCs/ and generates lib/npc-image-manifest.ts
 * mapping normalized slugs to image paths.
 *
 * Run: node scripts/generate-npc-image-manifest.mjs
 */
import { readdirSync, writeFileSync } from 'fs';
import { join, basename, extname } from 'path';

const dir = join(process.cwd(), 'public', 'images', 'NPCs');
const outFile = join(process.cwd(), 'lib', 'npc-image-manifest.ts');

let files;
try {
  files = readdirSync(dir).filter(f => /\.(png|jpe?g|webp|gif|svg)$/i.test(f));
} catch {
  files = [];
}

// Build the manifest: map normalized slugs → relative paths
const entries = new Map();

for (const file of files) {
  const name = basename(file, extname(file));            // "giant_spider"
  const relativePath = `images/NPCs/${file}`;

  // Key 1: strip all non-alphanumeric → "giantspider"
  const stripped = name.toLowerCase().replace(/[^a-z0-9]/g, '');
  // Key 2: keep underscores → "giant_spider"
  const withUnderscores = name.toLowerCase();

  // Don't overwrite an earlier entry (e.g. "ettercap" before "ettercap2")
  if (!entries.has(stripped)) entries.set(stripped, relativePath);
  if (withUnderscores !== stripped && !entries.has(withUnderscores)) {
    entries.set(withUnderscores, relativePath);
  }
}

// Sort keys for stable output
const sorted = [...entries.entries()].sort(([a], [b]) => a.localeCompare(b));
const lines = sorted.map(([key, path]) => `  '${key}': '${path}',`);

const output = `// Auto-generated by scripts/generate-npc-image-manifest.mjs — do not edit
const NPC_IMAGE_MANIFEST: Record<string, string> = {
${lines.join('\n')}
};

export default NPC_IMAGE_MANIFEST;
`;

writeFileSync(outFile, output, 'utf-8');
console.log(`Generated ${outFile} with ${entries.size} entries from ${files.length} files`);
